//장비했을 경우 스탯 계산
function checkitem() {

    p1.jbhp = 0
    p1.jbmp = 0
    p1.jbdmg = 0
    p1.jbdef = 0
    p1.jbspd = 0
    p1.jbhwp = 0

    p1.xhp = 1
    p1.xmp = 1
    p1.xdmg = 1
    p1.xdef = 1
    p1.xspd = 1

    p1.zsj = 0

    //h시리즈는 투구
    if (p1.hat === "h1") {
        p1.jbdef += 2
    }

    //a시리즈는 갑옷
    if (p1.armor === "a1") {
        p1.xhp++
    }

    //s시리즈는 신발
    if (p1.shoes === "s1") {
        //자신의 체력이 적보다 낮을 경우 공격속도 + 5

    }

    //g시리즈는 장갑
    if (p1.gloves === "g1") {
        //적에게 가하는 데미지 계산시 10% 확률로 데미지 2배

    }

    //p시리즈는 펜던트
    if (p1.pendant === "p1") {
        if (p1.hat === "h1" && p1.armor === "a1" && p1.shoes === "s1" && p1.gloves === "g1" && p1.wepon === "w1") {
            p1.jbhp += 10
            p1.jbmp += 10
            p1.jbdmg += 10
            p1.jbdef += 10
            p1.jbspd += 10
        }
    }

    //코어의 4냥꾼 시리즈
    if (p1.hat === "h4") {
        p1.jbspd += 2
    }
    if (p1.armor === "a4") {
        p1.jbdef += 1
        p1.jbspd += 2
    }
    if (p1.shoes === "s4") {
        p1.jbdef += 2
    }
    if (p1.gloves === "g4") {
        p1.jbspd += 2
    }
    if (p1.pendant === "p4") {
        p1.jbdmg += 2
        p1.jbspd += 2
    }
    if (p1.wepon === "wa4") {
        p1.jbdmg += 10
    }



    //w시리즈는 무기
    if (p1.wepon === "w1") {
        p1.jbdmg += 10
    }
    if (p1.wepon === "ww1") {
        p1.jbdmg += 5
    }
    if (p1.wepon === "wd1") {
        p1.jbdef += 5
    }
    if (p1.wepon === "wm1") {
        p1.jbdmg += 3
        p1.jbmp += 10
    }
    if (p1.wepon === "wr1") {
        p1.jbspd += 5
    }
    if (p1.wepon === "wa1") {
        p1.jbdmg += 7
    }

    //패시브
    if (p1.class === 'warrior') {
        p1.xhp += 0.5
        p1.jbdmg += 1
        p1.xhpzen += 0.5
    }
    if (p1.class === 'defender') {
        p1.jbdef += p1.lv + (p1.dmg * 0.5)
    }
    if (p1.class === 'magiccaster') {
        p1.xmp++
        p1.xmpzen++
    }
    if (p1.class === 'rogue') {
        p1.xspd += 0.5
        p1.xhp -= 0.5
        p1.jbhwp += 35
    }
    if (p1.class === 'archer') {
        p1.xdmg += 0.2
        p1.zsj = 1
    }
    if (p1.lv >= 1) { p1.maxsch = 1 }
    if (p1.lv >= 5) { p1.maxsch = 2 }
    if (p1.lv >= 10) { p1.maxsch = 3 }
    if (p1.lv >= 20) { p1.maxsch = 4 }
    if (p1.lv >= 30) { p1.maxsch = 5 }

    p1.fhp = (p1.hp + p1.jbhp) * p1.xhp
    p1.fmp = (p1.mp + p1.jbmp) * p1.xmp
    p1.fdmg = (p1.dmg + p1.jbdmg) * p1.xdmg
    p1.fdef = (p1.def + p1.jbdef) * p1.xdef
    p1.fspd = (p1.spd + p1.jbspd) * p1.xspd
    p1.fhwp = (p1.hwp + p1.jbhwp)



}

